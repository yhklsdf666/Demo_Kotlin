package com.yhklsdf.demo_kotlin.db;

public class Picture {

    private String url;

    private int weidh;

    private int height;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getWeidh() {
        return weidh;
    }

    public void setWeidh(int weidh) {
        this.weidh = weidh;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
